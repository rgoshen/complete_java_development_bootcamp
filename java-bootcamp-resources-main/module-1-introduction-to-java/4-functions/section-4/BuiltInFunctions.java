public class BuiltInFunctions {
    public static void main(String[] args) {
        double sine = Math.sin(1.2);
        double power = Math.pow(2, 4);
        double random = Math.floor(Math.random() * 10);

        System.out.println("Hello Java");
        System.out.println(Math.log(119.6));
        System.out.println("The sin of 1.2 is " + sine);
        System.out.println("2 to 4th power is " + power);
        System.out.println(random);
    }
}
